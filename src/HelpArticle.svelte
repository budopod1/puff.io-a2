<h2>Moving <b id="moving"></b></h2>

<p>Press A and D to move left and right. Press W to jump, and S to halt.</p>

<h2 id="inventory">Inventory</h2>

<p>You can open you inventory by pressing Space, which will show you all the blocks you have. The block you have selected is shown in the bottom left hand corner of the screen. You can change the selected block with either Q and E or the mouse wheel. Just because you have a block selected does not mean you have it in your inventory.</p>

<h2 id="breaking-and-placing">Breaking and Placing</h2>

<p>If you select a block that you have in your inventory you can place it by left clicking. To break a block, you can right click it or you can press shift and left click. Once you break a block, you have to wait a bit depending on the toughness of the block to recharge your wind energy. However, areas near the world spawn, are protected to stop new players getting stuck in a pit immediately.</p>

<h2 id="trading">Trading</h2>

<p>There is a trader very near the spawn point who you can buy things such as tools from. You can trade with them by clicking on them. Tools allow you to deal more damage, and break and reduce the time it takes to recharge your wind powers.</p>

<h2 id="enemies">Enemies</h2>

<p>Enemies will spawn frequently thought the world, you have a longer range for your wind powers then them, but be careful, there may be more than one! You can damage them by right clicking or left clicking and holding shift. If you let them get to close they can damage your wind power. You can see your wind power on the bottom right of your screen. You can recharge your wind power with <a href="#food">food</a>. If you run out of wind power you die and respawn without your inventory :(.</p>

<h2 id="food">Food</h2>

<p>When you break leaves of trees you have a chance to get mangos, which can either be planted to grow new trees, or eaten to increace your wind power.</p>

